// operator functions

function add(a,b) {
    return a + b;
}

function subtract(a,b) {
    return a - b;
}

function multiply(a,b) {
    return a * b;
}

function divide(a,b) {
    return a / b;
}

// operate function takes operator and numbers as arguments

function operate(operator,a,b) {
    return operator(a,b);
}
